package com.mentorship.calc;

public class Calculator {
    float a;
    float b;
    char operation;

    public float add() {
        return a + b;
    }
    public float sub() {
        return a - b;
    }
    public float mul() {
        return a * b;
    }
    public float div() {
        return a / b;
    }
}
